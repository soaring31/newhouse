<?php

namespace HouseBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Views
 *
 * @ORM\Table(name="views", indexes={@ORM\Index(name="name", columns={"name"}), @ORM\Index(name="bundle", columns={"bundle"}), @ORM\Index(name="controller", columns={"controller"})})
 * @ORM\Entity
 */
class Views
{
