     * @var string
     *
     * @ORM\Column(name="useform", type="string", length=100, nullable=false)
     */
    private $useform;

    /**
     * @var integer
     *
     * @ORM\Column(name="cache", type="smallint", length=2, nullable=false)
     */
    private $cache;

    /**
     * @var boolean
     *
     * @ORM\Column(name="checked", type="boolean", nullable=false)
     */
    private $checked;

    /**
     * @var string
     *
     * @ORM\Column(name="attributes", type="string", length=10, nullable=false)
     */
    private $attributes;

    /**
     * @var integer
     *
     * @ORM\Column(name="sort", type="integer", length=5, nullable=false)
     */
    private $sort;

    /**
     * @var boolean
     *
     * @ORM\Column(name="issystem", type="boolean", nullable=false)
