     * Set media_id
     *
     * @param string $mediaId
     * @return Files
     */
    public function setMediaId($mediaId)
    {
        $this->media_id = $mediaId;
    
        return $this;
    }

    /**
     * Get media_id
     *
     * @return string 
     */
    public function getMediaId()
    {
        return $this->media_id;
    }

